-- (<   >   <=  >=) exclusivo de números
-- (== ~=) não são exclusivos de números

-- 10 > 5 verdadeiro
-- 2 < 3 verdadeiro
-- 1 > 3 falsa
-- 3 > 3 falso
-- 3 <= 3 verdadeiro
-- 10 >= 11 falso

-- 10 == 10
-- 11 == 10
-- 10.1 == 10
-- 'eu sou uma string' ==  'eu sou uma string'

-- 'a' ~= 'b' verdadeiro
-- 'a' ~= 'a' falso

print(10 == 10)


