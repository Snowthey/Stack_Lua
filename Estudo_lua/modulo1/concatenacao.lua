--  concatenação é a união de duas strings

local nome = 'Matheus'
local emocao = 'feliz'

print('o meu nome e ' .. nome .. ' e estou ' .. emocao)
print('eu sou ' .. 'feliz')
print(nome .. emocao)

print('sua nota foi ' .. 10)

-- tentativa de concatenar uma string com nulo
print('sua nota foi ' .. nil)